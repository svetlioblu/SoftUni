function numberToText(arg) {
    let numb = Number(arg)

    if (numb === 1) {
        console.log('one')

    } else if (numb === 2) {
        console.log('two')

    } else if (numb === 3) {
        console.log('three')

    } else if (numb === 4) {
        console.log('four')

    } else if (numb === 5) {
        console.log('five')

    } else if (numb === 6) {
        console.log('six')

    } else if (numb === 7) {
        console.log('seven')

    } else if (numb === 8) {
        console.log('eight')

    } else if (numb === 9) {
        console.log('nine')

    } else {
        console.log('number too big')

    }

}
numberToText('10')